for (const [locale, translations] of Object.entries(criticalUITranslations)) {
  const localePath = path.join(localesDir, locale, 'translations.json');

  if (!fs.existsSync(localePath)) {
    console.log(`⚠️  ${locale}: File not found, skipping`);
    continue;
  }

  const currentTranslations = JSON.parse(fs.readFileSync(localePath, 'utf8'));

  let updatedCount = 0;
  for (const [key, value] of Object.entries(translations)) {
    if (currentTranslations[key] !== value) {
      currentTranslations[key] = value;
      updatedCount++;
    }
  }

  fs.writeFileSync(localePath, JSON.stringify(currentTranslations, null, 2) + '\n');
  console.log(`✅ ${locale}: Updated ${updatedCount} translations`);
}

